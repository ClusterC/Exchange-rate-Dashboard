# --- Data Fetching ---
@st.cache_data
def get_exchange_data(ticker, start_date, end_date, interval="1h"):  # Added interval parameter
    data = yf.Ticker(ticker)
    hist = data.history(start=start_date, end=end_date, interval=interval)  # Added interval to history
    return hist

